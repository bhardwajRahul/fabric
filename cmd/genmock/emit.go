// genmockMarker is the standard "code generated" line. The Go toolchain
// recognizes any line matching `^// Code generated .* DO NOT EDIT\.$` that
// precedes the package clause, marking the file as machine-generated.
const genmockMarker = "// Code generated by cmd/genmock. DO NOT EDIT."

// generatedLineRE matches a "code generated" marker line so a prior one is
// stripped from the reused header and re-emitted exactly once (idempotency).
var generatedLineRE = regexp.MustCompile(`(?m)^// Code generated .* DO NOT EDIT\.[ \t]*\r?\n?`)

// emit produces the gofmt'd mock.go source for g. If existing has a leading
// comment block (// or /* */ style) above the package clause, it is preserved
// verbatim. The tool does not synthesize a license header — operators add one
// once and the tool keeps it on every subsequent run.
func emit(g *generated, existing []byte) ([]byte, error) {
	var sb strings.Builder

	if h := reuseHeader(existing); h != "" {
		sb.WriteString(h)
		sb.WriteString("\n\n")
	}
	sb.WriteString(genmockMarker)
	sb.WriteString("\n\n")
	sb.WriteString("package ")
	sb.WriteString(g.pkgName)
	sb.WriteString("\n\n")

	writeImports(&sb, g.imports, g.apiPath)

	ordered := orderMethods(g.methods)

	writeStruct(&sb, g, ordered)
	writeNewMock(&sb)
	writeLifecycle(&sb)

	for _, m := range ordered {
		if m.isGraph {
			writeGraphMock(&sb, g, m)
		} else {
			writeStandardMock(&sb, m)
		}
	}

	src := []byte(sb.String())
	out, err := format.Source(src)
	if err != nil {
		return nil, fmt.Errorf("gofmt: %w\nsource:\n%s", err, src)
	}
	return out, nil
}

// reuseHeader returns the leading comment block from existing, or empty if
// existing is absent or has no leading comment. The tool does not synthesize a
// license header.
func reuseHeader(existing []byte) string {
	if len(existing) == 0 {
		return ""
	}
	// The header is everything before the first `package` keyword at column 0.
	idx := bytes.Index(existing, []byte("\npackage "))
	if idx < 0 {
		return ""
	}
	// Strip any prior "code generated" marker so it is re-emitted exactly once.
	hdr := generatedLineRE.ReplaceAllString(string(existing[:idx]), "")
	return strings.TrimRight(hdr, " \t\n")
}

// writeImports emits a 3-group import block: stdlib, then framework / third-
// party, then (separated by a blank line) the service's own api package.
func writeImports(sb *strings.Builder, imports map[string]string, ownAPI string) {
	var std, thirdParty []string
	for path := range imports {
		if path == ownAPI {
			continue
		}
		if isStdLib(path) {
			std = append(std, path)
		} else {
			thirdParty = append(thirdParty, path)
		}
	}
	sort.Strings(std)
	sort.Strings(thirdParty)

	sb.WriteString("import (\n")
	for _, p := range std {
		fmt.Fprintf(sb, "\t%q\n", p)
	}
	if len(std) > 0 && len(thirdParty) > 0 {
		sb.WriteString("\n")
	}
	for _, p := range thirdParty {
		fmt.Fprintf(sb, "\t%q\n", p)
	}
	if ownAPI != "" && imports[ownAPI] != "" || (ownAPI != "" && hasKey(imports, ownAPI)) {
		if len(std) > 0 || len(thirdParty) > 0 {
			sb.WriteString("\n")
		}
		fmt.Fprintf(sb, "\t%q\n", ownAPI)
	}
	sb.WriteString(")\n\n")
}

func hasKey(m map[string]string, k string) bool {
	_, ok := m[k]
	return ok
}

func isStdLib(path string) bool {
	// Standard library paths have no dot in the first segment.
	first := path
	if idx := strings.Index(path, "/"); idx >= 0 {
		first = path[:idx]
	}
	return !strings.Contains(first, ".")
}

// orderMethods returns methods in emission order: non-OnChanged callbacks
// first (in declaration order), then OnChanged callbacks at the end. This
// matches the long-standing convention in hand-written mock.go files.
func orderMethods(ms []method) []method {
	var main, callbacks []method
	for _, m := range ms {
		if strings.HasPrefix(m.name, "OnChanged") {
			callbacks = append(callbacks, m)
		} else {
			main = append(main, m)
		}
	}
	return append(main, callbacks...)
}

// writeStruct emits the Mock struct: embedded *Intermediate plus one mockX (or
// mockXGraph + unsubMockX) field per non-lifecycle ToDo method.
func writeStruct(sb *strings.Builder, g *generated, ordered []method) {
	sb.WriteString("// Mock is a mockable version of the microservice, allowing functions, event sinks and web handlers to be mocked.\n")
	sb.WriteString("type Mock struct {\n")
	sb.WriteString("\t*Intermediate\n")
	for _, m := range ordered {
		if m.isGraph {
			fmt.Fprintf(sb, "\tmock%sGraph func(ctx context.Context) (graph *workflow.Graph, err error) // MARKER: %s\n",
				m.name, m.marker)
			fmt.Fprintf(sb, "\tunsubMock%s func() error // MARKER: %s\n", m.name, m.marker)
			continue
		}
		fmt.Fprintf(sb, "\tmock%s func(%s) (%s) // MARKER: %s\n",
			m.name, paramList(m.params), paramList(m.results), m.marker)
	}
	sb.WriteString("}\n\n")
}

func writeNewMock(sb *strings.Builder) {
	sb.WriteString(`// NewMock creates a new mockable version of the microservice.
func NewMock() *Mock {
	svc := &Mock{}
	svc.Intermediate = NewIntermediate(svc)
	svc.SetVersion(7357) // Stands for TEST
	return svc
}

`)
}

func writeLifecycle(sb *strings.Builder) {
	sb.WriteString(`// OnStartup is called when the microservice is started up.
func (svc *Mock) OnStartup(ctx context.Context) (err error) {
	if svc.Deployment() != connector.LOCAL && svc.Deployment() != connector.TESTING {
		return errors.New("mocking disallowed in %s deployment", svc.Deployment())
	}
	return nil
}

// OnShutdown is called when the microservice is shut down.
func (svc *Mock) OnShutdown(ctx context.Context) (err error) {
	return nil
}

`)
}

// writeStandardMock emits the MockX setter and X executor for a normal method.
func writeStandardMock(sb *strings.Builder, m method) {
	fmt.Fprintf(sb, "// Mock%s sets up a mock handler for %s.\n", m.name, m.name)
	fmt.Fprintf(sb, "func (svc *Mock) Mock%s(handler func(%s) (%s)) *Mock { // MARKER: %s\n",
		m.name, paramList(m.params), paramList(m.results), m.marker)
	fmt.Fprintf(sb, "\tsvc.mock%s = handler\n", m.name)
	sb.WriteString("\treturn svc\n}\n\n")

	fmt.Fprintf(sb, "// %s executes the mock handler.\n", m.name)
	fmt.Fprintf(sb, "func (svc *Mock) %s(%s) (%s) { // MARKER: %s\n",
		m.name, paramList(m.params), paramList(m.results), m.marker)

	if singleErrResult(m.results) {
		fmt.Fprintf(sb, "\tif svc.mock%s != nil {\n", m.name)
		fmt.Fprintf(sb, "\t\terr = svc.mock%s(%s)\n", m.name, argList(m.params))
		sb.WriteString("\t}\n")
		sb.WriteString("\treturn errors.Trace(err)\n")
	} else {
		fmt.Fprintf(sb, "\tif svc.mock%s != nil {\n", m.name)
		fmt.Fprintf(sb, "\t\t%s = svc.mock%s(%s)\n", resultLValues(m.results), m.name, argList(m.params))
		sb.WriteString("\t}\n")
		fmt.Fprintf(sb, "\treturn %s, errors.Trace(err)\n", resultNamesWithoutErr(m.results))
	}
	sb.WriteString("}\n\n")
}

// writeGraphMock emits the workflow-graph mock variant - a synthetic subgraph
// that replaces the workflow with a single mock task delegating to a typed
// handler.
func writeGraphMock(sb *strings.Builder, g *generated, m method) {
	pair, ok := g.graphPairs[m.name]
	if !ok {
		// Without In/Out structs the typed handler can't be rendered; fall back
		// to the standard pattern.
		writeStandardMock(sb, m)
		return
	}

	apiPkg := g.apiPkg
	handlerSig := buildHandlerSig(pair, apiPkg)
	handlerInArgs := buildHandlerInArgs(pair)
	handlerOutNames := buildHandlerOutNames(pair)
	outStructLit := buildOutStructLit(pair, apiPkg, m.name)
	routeKebab := kebabCase(m.name)

	fmt.Fprintf(sb, "// Mock%s sets up a mock handler for the %s workflow.\n", m.name, m.name)
	sb.WriteString("// The handler receives typed inputs from the workflow's state and returns typed outputs.\n")
	sb.WriteString("// A nil handler clears the mock.\n")
	fmt.Fprintf(sb, "func (svc *Mock) Mock%s(handler %s) *Mock { // MARKER: %s\n",
		m.name, handlerSig, m.marker)
	fmt.Fprintf(sb, "\tif svc.unsubMock%s != nil {\n", m.name)
	fmt.Fprintf(sb, "\t\tsvc.unsubMock%s()\n", m.name)
	fmt.Fprintf(sb, "\t\tsvc.unsubMock%s = nil\n", m.name)
	sb.WriteString("\t}\n")
	sb.WriteString("\tif handler == nil {\n")
	fmt.Fprintf(sb, "\t\tsvc.mock%sGraph = nil\n", m.name)
	sb.WriteString("\t\treturn svc\n")
	sb.WriteString("\t}\n")
	fmt.Fprintf(sb, "\tmockName := \"Mock%s\" + utils.RandomIdentifier(8)\n", m.name)
	fmt.Fprintf(sb, "\tmockRoute := \":428/mock-%s-\" + utils.RandomIdentifier(8)\n", routeKebab)
	sb.WriteString("\tmockTaskURL := httpx.JoinHostAndPath(svc.Hostname(), mockRoute)\n")
	fmt.Fprintf(sb, "\tsvc.mock%sGraph = func(ctx context.Context) (graph *workflow.Graph, err error) {\n", m.name)
	fmt.Fprintf(sb, "\t\tg := workflow.NewGraph(%q, %s.%s.URL())\n", m.name, apiPkg, m.name)
	sb.WriteString("\t\tg.AddTransition(mockTaskURL, workflow.END)\n")
	sb.WriteString("\t\treturn g, nil\n")
	sb.WriteString("\t}\n")
	sb.WriteString("\terr := svc.Subscribe(mockName, func(w http.ResponseWriter, r *http.Request) error {\n")
	sb.WriteString("\t\tvar f workflow.Flow\n")
	sb.WriteString("\t\tif err := json.NewDecoder(r.Body).Decode(&f); err != nil {\n")
	sb.WriteString("\t\t\treturn errors.Trace(err)\n")
	sb.WriteString("\t\t}\n")
	sb.WriteString("\t\tsnap := f.Snapshot()\n")
	fmt.Fprintf(sb, "\t\tvar in %s.%sIn\n", apiPkg, m.name)
	sb.WriteString("\t\tf.ParseState(&in)\n")
	fmt.Fprintf(sb, "\t\t%s := handler(r.Context(), &f%s)\n", handlerOutNames, handlerInArgs)
	sb.WriteString("\t\tif err != nil {\n")
	sb.WriteString("\t\t\treturn err // No trace\n")
	sb.WriteString("\t\t}\n")
	fmt.Fprintf(sb, "\t\tout := %s\n", outStructLit)
	sb.WriteString("\t\tf.SetChanges(out, snap)\n")
	sb.WriteString("\t\tw.Header().Set(\"Content-Type\", \"application/json\")\n")
	sb.WriteString("\t\treturn json.NewEncoder(w).Encode(&f)\n")
	sb.WriteString("\t},\n")
	sb.WriteString("\t\tsub.At(\"POST\", mockRoute),\n")
	fmt.Fprintf(sb, "\t\tsub.Task(%s.%sIn{}, %s.%sOut{}),\n", apiPkg, m.name, apiPkg, m.name)
	sb.WriteString("\t)\n")
	sb.WriteString("\tif err == nil {\n")
	fmt.Fprintf(sb, "\t\tsvc.unsubMock%s = func() error { return svc.Unsubscribe(mockName) }\n", m.name)
	sb.WriteString("\t}\n")
	sb.WriteString("\treturn svc\n}\n\n")

	fmt.Fprintf(sb, "// %s returns the workflow graph, or a mocked graph if Mock%s was called.\n", m.name, m.name)
	fmt.Fprintf(sb, "func (svc *Mock) %s(ctx context.Context) (graph *workflow.Graph, err error) { // MARKER: %s\n",
		m.name, m.marker)
	fmt.Fprintf(sb, "\tif svc.mock%sGraph != nil {\n", m.name)
	fmt.Fprintf(sb, "\t\tgraph, err = svc.mock%sGraph(ctx)\n", m.name)
	sb.WriteString("\t}\n")
	sb.WriteString("\treturn graph, errors.Trace(err)\n")
	sb.WriteString("}\n\n")
}

// paramList renders a list of params as a comma-separated `name type` sequence.
func paramList(ps []param) string {
	var parts []string
	for _, p := range ps {
		if p.name == "" {
			parts = append(parts, p.typ)
		} else {
			parts = append(parts, p.name+" "+p.typ)
		}
	}
	return strings.Join(parts, ", ")
}

// argList renders a list of params as a comma-separated `name` sequence.
func argList(ps []param) string {
	var parts []string
	for _, p := range ps {
		parts = append(parts, p.name)
	}
	return strings.Join(parts, ", ")
}

// resultLValues renders the result names (excluding err which is reassigned
// separately) followed by `err`, suitable for the LHS of an assignment from a
// handler call. E.g. params with (token, err) -> "token, err".
func resultLValues(ps []param) string {
	var parts []string
	for _, p := range ps {
		parts = append(parts, p.name)
	}
	return strings.Join(parts, ", ")
}

// resultNamesWithoutErr renders the result names with err removed (used for
// the `return r0, r1, errors.Trace(err)` line).
func resultNamesWithoutErr(ps []param) string {
	var parts []string
	for _, p := range ps {
		if p.name == "err" && p.typ == "error" {
			continue
		}
		parts = append(parts, p.name)
	}
	return strings.Join(parts, ", ")
}

// singleErrResult reports whether the result list is just `(err error)`.
func singleErrResult(ps []param) bool {
	return len(ps) == 1 && ps[0].name == "err" && ps[0].typ == "error"
}

// buildHandlerSig renders the workflow-graph handler's func-type signature.
func buildHandlerSig(pair inOutPair, apiPkg string) string {
	var sb strings.Builder
	sb.WriteString("func(ctx context.Context, flow *workflow.Flow")
	for _, f := range pair.in {
		sb.WriteString(", ")
		sb.WriteString(f.goName)
		sb.WriteByte(' ')
		sb.WriteString(qualifyTypes(f.typ, apiPkg))
	}
	sb.WriteString(") (")
	for _, f := range pair.out {
		sb.WriteString(f.goName)
		sb.WriteByte(' ')
		sb.WriteString(qualifyTypes(f.typ, apiPkg))
		sb.WriteString(", ")
	}
	sb.WriteString("err error)")
	return sb.String()
}

// qualifyTypes prefixes bare type identifiers in a Go type expression with the
// api package alias. It leaves builtins, already-qualified selectors, and
// keywords alone. Used when rendering In/Out field types (declared inside the
// api package) into mock.go (which lives one directory up).
func qualifyTypes(typ, apiPkg string) string {
	if apiPkg == "" {
		return typ
	}
	var sb strings.Builder
	i := 0
	for i < len(typ) {
		c := typ[i]
		if !isIdentStart(c) {
			sb.WriteByte(c)
			i++
			continue
		}
		j := i
		for j < len(typ) && isIdentCont(typ[j]) {
			j++
		}
		ident := typ[i:j]
		precededByDot := i > 0 && typ[i-1] == '.'
		followedByDot := j < len(typ) && typ[j] == '.'
		if precededByDot || followedByDot || isBuiltinOrKeyword(ident) {
			sb.WriteString(ident)
		} else {
			sb.WriteString(apiPkg)
			sb.WriteByte('.')
			sb.WriteString(ident)
		}
		i = j
	}
	return sb.String()
}

var builtinTypes = map[string]bool{
	"bool": true, "byte": true, "complex64": true, "complex128": true,
	"error": true, "float32": true, "float64": true,
	"int": true, "int8": true, "int16": true, "int32": true, "int64": true,
	"rune": true, "string": true,
	"uint": true, "uint8": true, "uint16": true, "uint32": true, "uint64": true, "uintptr": true,
	"any": true, "true": true, "false": true, "nil": true,
	// Type keywords / composite shapes (don't qualify).
	"map": true, "chan": true, "func": true, "interface": true, "struct": true,
}

func isBuiltinOrKeyword(s string) bool { return builtinTypes[s] }

// buildHandlerInArgs renders `, in.Field0, in.Field1, ...` for the body's call
// to handler. Leading comma is included so the caller can append after `&f`.
func buildHandlerInArgs(pair inOutPair) string {
	var sb strings.Builder
	for _, f := range pair.in {
		sb.WriteString(", in.")
		sb.WriteString(f.rawGo)
	}
	return sb.String()
}

// buildHandlerOutNames renders `out0, out1, ..., err` for the LHS of the
// handler-call assignment.
func buildHandlerOutNames(pair inOutPair) string {
	var parts []string
	for _, f := range pair.out {
		parts = append(parts, f.goName)
	}
	parts = append(parts, "err")
	return strings.Join(parts, ", ")
}

// buildOutStructLit renders `apiPkg.<name>Out{RawGoField0: outArg0, ...}`.
func buildOutStructLit(pair inOutPair, apiPkg, methodName string) string {
	var sb strings.Builder
	fmt.Fprintf(&sb, "%s.%sOut{", apiPkg, methodName)
	for i, f := range pair.out {
		if i > 0 {
			sb.WriteString(", ")
		}
		sb.WriteString(f.rawGo)
		sb.WriteString(": ")
		sb.WriteString(f.goName)
	}
	sb.WriteByte('}')
	return sb.String()
}

// kebabCase converts a PascalCase identifier to kebab-case.
func kebabCase(s string) string {
	var sb strings.Builder
	for i, r := range s {
		if r >= 'A' && r <= 'Z' {
			if i > 0 {
				sb.WriteByte('-')
			}
			sb.WriteRune(r + ('a' - 'A'))
		} else {
			sb.WriteRune(r)
		}
	}
	return sb.String()
}
