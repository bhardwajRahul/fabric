/*
ManagerOnly_Get performs a GET request to the ManagerOnly endpoint.

ManagerOnly is only accessible by managers.

If a URL is not provided, it defaults to the URL of the endpoint. Otherwise, it is resolved relative to the URL of the endpoint.
*/
func ManagerOnly_Get(t *testing.T, ctx context.Context, url string) *ManagerOnlyTestCase {
	tc := &ManagerOnlyTestCase{t: t}
	var err error
	url, err = httpx.ResolveURL(loginapi.URLOfManagerOnly, url)
	if err != nil {
		tc.err = errors.Trace(err)
		return tc
	}
	url, err = httpx.FillPathArguments(url)
	if err != nil {
		tc.err = errors.Trace(err)
		return tc
	}
	r, err := http.NewRequest("GET", url, nil)
	if err != nil {
		tc.err = errors.Trace(err)
		return tc
	}
	ctx = frame.CloneContext(ctx)
	r = r.WithContext(ctx)
	r.Header = frame.Of(ctx).Header()
	w := httpx.NewResponseRecorder()
	t0 := time.Now()
	tc.err = errors.CatchPanic(func() error {
		return Svc.ManagerOnly(w, r)
	})
	tc.dur = time.Since(t0)
	tc.res = w.Result()
	return tc
}

