const (
	Hostname = shardedflowapi.Hostname
	Version  = 1
)

// ToDo is implemented by the service or mock.
type ToDo interface {
	OnStartup(ctx context.Context) (err error)
	OnShutdown(ctx context.Context) (err error)
	Record(ctx context.Context, flow *workflow.Flow) (recorded bool, err error) // MARKER: Record
	Sharded(ctx context.Context) (graph *workflow.Graph, err error)             // MARKER: Sharded
}

// NewService creates a new instance of the microservice.
func NewService() *Service {
	svc := &Service{}
	svc.Intermediate = NewIntermediate(svc)
	return svc
}

// Init enables a single-statement pattern for initializing the microservice.
func (svc *Service) Init(initializer func(svc *Service) (err error)) *Service {
	svc.Connector.Init(func(_ *connector.Connector) (err error) {
		return initializer(svc)
	})
	return svc
}

// Intermediate extends and customizes the generic base connector.
type Intermediate struct {
	*connector.Connector
	ToDo
}

// NewIntermediate creates a new instance of the intermediate.
func NewIntermediate(impl ToDo) *Intermediate {
	svc := &Intermediate{
		Connector: connector.New(Hostname),
		ToDo:      impl,
	}
	svc.SetVersion(Version)
	svc.SetDescription(`shardedflow.verify exercises cross-shard global priority+fairness aggregation (NumShards>1).`)
	svc.SetOnStartup(svc.OnStartup)
	svc.SetOnShutdown(svc.OnShutdown)
	svc.SetResFS(resources.FS)
	svc.SetOnObserveMetrics(svc.doOnObserveMetrics)
	svc.SetOnConfigChanged(svc.doOnConfigChanged)

	svc.Subscribe( // MARKER: Record
		"Record", svc.doRecord,
		sub.At(shardedflowapi.Record.Method, shardedflowapi.Record.Route),
		sub.Description(`Record sleeps for the requested delay and appends its tag to the completion order.`),
		sub.Task(shardedflowapi.RecordIn{}, shardedflowapi.RecordOut{}),
	)

	svc.Subscribe( // MARKER: Sharded
		"Sharded", svc.doSharded,
		sub.At(shardedflowapi.Sharded.Method, shardedflowapi.Sharded.Route),
		sub.Description(`Sharded defines the single-task graph used to observe cross-shard global dispatch order.`),
		sub.Workflow(shardedflowapi.ShardedIn{}, shardedflowapi.ShardedOut{}),
	)

	_ = marshalFunction
	return svc
}

func (svc *Intermediate) doOnObserveMetrics(ctx context.Context) (err error) {
	return svc.Parallel()
}

func (svc *Intermediate) doOnConfigChanged(ctx context.Context, changed func(string) bool) (err error) {
	return nil
}

func marshalFunction(w http.ResponseWriter, r *http.Request, route string, in any, out any, execute func(in any, out any) error) error {
	err := httpx.ReadInputPayload(r, route, in)
	if err != nil {
		return errors.Trace(err)
	}
	err = execute(in, out)
	if err != nil {
		return err
	}
	err = httpx.WriteOutputPayload(w, out)
	if err != nil {
		return errors.Trace(err)
	}
	return nil
}

// doRecord handles marshaling for Record.
func (svc *Intermediate) doRecord(w http.ResponseWriter, r *http.Request) (err error) { // MARKER: Record
	var flow workflow.Flow
	err = json.NewDecoder(r.Body).Decode(&flow)
	if err != nil {
		return errors.Trace(err)
	}
	snap := flow.Snapshot()
	var in shardedflowapi.RecordIn
	flow.ParseState(&in)
	var out shardedflowapi.RecordOut
	out.Recorded, err = svc.Record(r.Context(), &flow)
	if err != nil {
		return err
	}
	flow.SetChanges(out, snap)
	w.Header().Set("Content-Type", "application/json")
	return errors.Trace(json.NewEncoder(w).Encode(&flow))
}

// doSharded handles marshaling for Sharded.
func (svc *Intermediate) doSharded(w http.ResponseWriter, r *http.Request) (err error) { // MARKER: Sharded
	graph, err := svc.Sharded(r.Context())
	if err != nil {
		return err
	}
	err = graph.Validate()
	if err != nil {
		return errors.Trace(err)
	}
	w.Header().Set("Content-Type", "application/json")
	return errors.Trace(json.NewEncoder(w).Encode(struct {
		Graph *workflow.Graph `json:"graph"`
	}{Graph: graph}))
}
